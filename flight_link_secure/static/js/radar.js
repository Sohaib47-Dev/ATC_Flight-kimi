/**
 * Convert Geographic (lat, lon) to SVG Coordinates
 * @param {number} lat - Latitude
 * @param {number} lon - Longitude
 * @returns {object} {x, y} SVG coordinates
 */
function geoToSVG(lat, lon) {
    // Define bounds for Pakistan FIR
    const latMin = 23.0, latMax = 37.5;
    const lonMin = 60.5, lonMax = 75.5;

    // SVG canvas dimensions
    const svgWidth = 900;
    const svgHeight = 650;

    // Add padding
    const padding = 50;

    // Normalize to 0-1 range
    const normalizedX = (lon - lonMin) / (lonMax - lonMin);
    const normalizedY = (lat - latMin) / (latMax - latMin);

    // Convert to SVG coordinates (flip Y axis)
    const x = padding + normalizedX * (svgWidth - 2 * padding);
    const y = svgHeight - padding - normalizedY * (svgHeight - 2 * padding);

    return { x, y };
}

/**
 * Convert Real Pakistan FIR Boundary to SVG Polygon Points
 * @returns {string} SVG polygon points string
 */
function getFIRBoundaryPoints() {
    return PAKISTAN_FIR_REAL_COORDS
        .map(coord => {
            const svg = geoToSVG(coord.lat, coord.lon);
            return `${svg.x},${svg.y}`;
        })
        .join(' ');
}

