/**
 * Fallback when getScreenCTM is unavailable: map client pixels to user space
 * for viewBox + preserveAspectRatio xMidYMid meet (matches #radarSvg).
 */
function radarDebugClientToSvgUserMeetFallback(svg, clientX, clientY) {
    const vb = svg.viewBox && svg.viewBox.baseVal;
    if (!vb || vb.width <= 0 || vb.height <= 0) return null;
    const rect = svg.getBoundingClientRect();
    if (rect.width <= 0 || rect.height <= 0) return null;
    const scale = Math.min(rect.width / vb.width, rect.height / vb.height);
    const dispW = vb.width * scale;
    const dispH = vb.height * scale;
    const offX = rect.left + (rect.width - dispW) / 2;
    const offY = rect.top + (rect.height - dispH) / 2;
    const x = vb.x + (clientX - offX) / scale;
    const y = vb.y + (clientY - offY) / scale;
    return { x, y };
}

/**
 * Client (viewport) pixel position → SVG user space (same units as geoToSVG).
 * Does not alter any existing radar code paths.
 */
function radarDebugClientToSvgUser(svg, clientX, clientY) {
    if (!svg) return null;
    if (typeof svg.createSVGPoint === 'function') {
        const ctm = svg.getScreenCTM();
        if (ctm) {
            try {
                const pt = svg.createSVGPoint();
                pt.x = clientX;
                pt.y = clientY;
                const svgPt = pt.matrixTransform(ctm.inverse());
                return { x: svgPt.x, y: svgPt.y };
            } catch (e) {
                console.warn('[radar coord debug] CTM inverse failed, using meet fallback:', e);
            }
        }
    }
    return radarDebugClientToSvgUserMeetFallback(svg, clientX, clientY);
}

/**
 * Inverse of geoToSVG(x,y). Numeric literals MUST stay in sync with geoToSVG.
 */
function radarUnprojectToLatLon(x, y) {
    const latMin = 23.0;
    const latMax = 37.5;
    const lonMin = 60.5;
    const lonMax = 75.5;
    const svgWidth = 900;
    const svgHeight = 650;
    const padding = 50;
    const innerW = svgWidth - 2 * padding;
    const innerH = svgHeight - 2 * padding;
    const normalizedX = (x - padding) / innerW;
    const normalizedY = (svgHeight - padding - y) / innerH;
    const lon = lonMin + normalizedX * (lonMax - lonMin);
    const lat = latMin + normalizedY * (latMax - latMin);
    return { lat, lon };
}

