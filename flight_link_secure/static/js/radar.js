function normalizeFirEntryKeyForCoords(firKey) {
    if (firKey == null || firKey === '') return firKey;
    const k = String(firKey).trim().toUpperCase();
    if (k === 'DODAT') return 'DOBAT';
    return k;
}

/**
 * Closest point on segment AB to P in planar lon/lat space (degrees).
 * @returns {{ x: number, y: number, distSq: number }} closest point (lon, lat) and squared distance
 */
function closestPointOnSegmentLonLat(px, py, ax, ay, bx, by) {
    const abx = bx - ax;
    const aby = by - ay;
    const apx = px - ax;
    const apy = py - ay;
    const ab2 = abx * abx + aby * aby;
    if (ab2 < 1e-18) {
        const dx = px - ax;
        const dy = py - ay;
        return { x: ax, y: ay, distSq: dx * dx + dy * dy };
    }
    let t = (apx * abx + apy * aby) / ab2;
    t = Math.max(0, Math.min(1, t));
    const qx = ax + t * abx;
    const qy = ay + t * aby;
    const dx = px - qx;
    const dy = py - qy;
    return { x: qx, y: qy, distSq: dx * dx + dy * dy };
}

/**
 * Snap a lat/lon point to the nearest point on the FIR boundary polyline.
 */
function snapLatLonToPolyline(lat, lon, polyline) {
    if (!polyline || polyline.length < 2) {
        return { lat, lon };
    }
    const px = lon;
    const py = lat;
    let bestLat = lat;
    let bestLon = lon;
    let bestDistSq = Infinity;
    for (let i = 0; i < polyline.length - 1; i++) {
        const a = polyline[i];
        const b = polyline[i + 1];
        const seg = closestPointOnSegmentLonLat(px, py, a.lon, a.lat, b.lon, b.lat);
        if (seg.distSq < bestDistSq) {
            bestDistSq = seg.distSq;
            bestLon = seg.x;
            bestLat = seg.y;
        }
    }
    return { lat: bestLat, lon: bestLon };
}

